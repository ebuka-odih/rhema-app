export const isJesusSpeaking = (book: string, chapter: number | string, verse: number | string): boolean => {
    const chapterStr = chapter.toString();
    const verseNum = typeof verse === 'string' ? parseInt(verse) : verse;

    if (RED_LETTER_MAPPING[book] && RED_LETTER_MAPPING[book][chapterStr]) {
        return RED_LETTER_MAPPING[book][chapterStr].includes(verseNum);
    }
    return false;
};
